function go(sel) {

  if (sel == 1) {
    window.location.href = "Acceso.html";
  }
  else if (sel == 2) {
    window.location.href = "Registro.html";
  }
  else if (sel == 3) {
    window.location.href = "Ok.html";
  }
  else if (sel == 4) {
    window.location.href = "Menu.html";
  }
}